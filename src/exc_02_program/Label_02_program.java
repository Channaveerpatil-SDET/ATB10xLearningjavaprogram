package exc_02_program;

public class Label_02_program {

    public static void main(String[] args) {

        int age=10;
        age=89;
        System.out.println("age :"+age);
        byte a= 20;
        short b = 500;
        int c= 40000;
        long d= 100000L;
        char e = 'c';
        boolean  f = true;
        double g= 78.996767676;
        float h = 3.142f;
        System.out.println("byte value is :" +a);
        System.out.println("Short value is :" +b);
        System.out.println("int value is :"  +c);
        System.out.println("long value is : " +d);
        System.out.println("char value is : " +e);
        System.out.println("boolean value is : " +f);
        System.out.println("double value is : " +g);
        System.out.println("float value is : " +h);



    }
}
