public class label_09_while_loop_naturnal_number_program {

    public static void main(String[] args) {

        int i=1;
         int sum =0;
        while(i<=10){

            sum= sum+i;

            i++;
        }

        System.out.println("Total sum value is " + sum);
    }
}
