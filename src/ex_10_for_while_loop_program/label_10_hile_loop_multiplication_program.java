package ex_10_for_while_loop_program;

public class label_10_hile_loop_multiplication_program {


    public static void main(String[] args) {

        int number=7;
        int i=1;

        while(i<=10){

            System.out.println(number+ " * "+ i +" = "+ (number*i));
            i++;
        }
    }


}
