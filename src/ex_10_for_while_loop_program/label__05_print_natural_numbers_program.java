package ex_10_for_while_loop_program;

public class label__05_print_natural_numbers_program {

    public static void main(String[] args) {

        int sum=0;

        for(int i=1; i<=10; i++){
            sum = sum+i;

        }
        System.out.println("The sum of the first 10 natural numbers is: " + sum);

    }
}
